print('*** Ciclo While ***')

# Imprimir los valores del 1 al 5

contador = 1
while contador <= 5:
    print(contador, end=' ') # Le estoy dando un formato de impresión por cada línea que termina de imprimirse, en este caso, solo deja un espacio
    # print(contador, end=' - ') # En este caso me dejaría un espacio seguido de un guión
    contador += 1 # contador = contador + 1
