# Buscar subcadenas

cadena = 'Hola, Mundo!'
cadena_normal = cadena.lower()
indice = cadena_normal.find("mundo")

print(f"\nMani, el indice donde se encuentra la palabra \"mundo\" en la cadena {cadena} es: {indice}")

# Obtener el indice de la variable hola

indice = cadena.lower().find("hola")
print(f"\nMani, el indice donde se encuentra la palabra \"hola\" en la cadena {cadena} es: {indice}")