# Variables en Python

# Declaración e inicialización de variables en Python (obligatoriamente se declara y asigna)
edad = 19
altura = 1.78
pais = "Colombia - Canadá"

# Acceder al valor de las variables
print("La edad del usuario es:",edad)
print("La altura del usuario es:",altura)
print("El país del usuario es:",pais)

# Modificar el valor de una variable
edad = 20
altura = 1.80
pais = "Senegal"

print("Edad nueva:",edad)
print("Altura nueva:",altura)
print("Pais nuevo:",pais)

# En python el tipo de variables es dinamico (Pueden cambiar de cualquier tipo de valor)
edad = "Veinte"
print("La edad es:",edad)

# Si se desea acceder a una variable no declarada ocurrirá un error
telefono = 3012299159
print(telefono)