print(f"*** Operadores de comparaciÃ³n ***")

a, b = 7,5
print(f"\nEl valor inicial de a es: {a}, el valor inicial de b es: {b}")

# Operador de igualdad ==
resultado = (a == b)
print(f"\nEl valor del resultado a == b es: {resultado}")

# Operador diferente !=
resultado = (a != b)
print(f"\nEl valor del resultado a != b es: {resultado}")

# Operador mayor o igual que >=
resultado = a >= b
print(f"\nEl valor del resultado a >= b es: {resultado}")

# Operador menor que  <
resultado = a < b
print(f"\nEl valor del resultado a < b es: {resultado}")

# Operador menor o igual que <=
resultado = a <= b
print(f"\nEl valor del resultado a <= b es: {resultado}")

