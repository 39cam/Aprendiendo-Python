# Ejemplo tipos datos

# Ejemplo de concatenación o suma de valores segun el tipo de dato a trabajar

numero1_cadena = "10"
print(f"Variable Numero1 en cadena: {numero1_cadena}")
numero2_cadena = "20"
print(f"Variable Numero2 en cadena: {numero2_cadena}")

resultado = numero1_cadena + numero2_cadena
print(f"El resultado es: {resultado}\n") # Esto no es operación aritmética, se trata de concatenación de ambas: 10 + 20 = 1020 XD

# Convertimos a tipos enteros

numero1 = int(numero1_cadena)
print(f"El número 1 es: {numero1}")
numero2 = int(numero2_cadena)
print(f"El número 2 es: {numero2}")
resultado = numero1 + numero2
print(f"El resultado es: {resultado}") # Esto si es una operación aritmética


